					<h2 id="Bélier">Bélier<h2>
					<h3>Le confiant</h3>
					<p>Les natifs du Bélier sont considérées comme des personnes ouvertes, actives, enthousiastes, indépendantes et qui aiment l’aventure. Bien qu’ils soient nés pour être des leaders, les Béliers peuvent être parfois impulsifs, agressifs et autosuffisants.</p>
					
					<h2 id="Taureau">Taureau</h2>
					<h3>L'incontournable</h3>
					<p>Les natifs du signe astrologique du Taureau sont considérés comme loyaux et fiables, généreux avec le temps et l’argent, aspirent à la sécurité et à la stabilité. Les personnes nées sous ce signe du zodiaque sont généralement têtues, possessives et peuvent parfois sembler renfermées sur elles même.</p>
					
					
					<h2 id="Gémeaux">Gémeaux</h2>
					<h3>Le communicateur</h3>					
					<p>Les personnes nées sous le signe des Gémeaux sont imaginatives, intelligentes et créatives, mais c’est aussi le signe le plus bavard de des signes du zodiaque sûrement due à leurs talents innés pour la communication. Les Gémeaux peuvent aussi être superficiels, sournois, d’humeur changeante voir impulsif.</p>



					<h2 id="Cancer">Cancer</h2>
					<h3>Le sensible</h3>
					<p>Les Cancers sont loyaux et dignes de confiance, rêveurs, attentionnés et plein de compassion. Ils peuvent être lunatique, plein d’insécurité et égocentrique.</p>

					<h2 id="Lion">Lion</h2>
					<h3>Le puissant</h3>
					<p>Les natifs du signe astrologique du Lion sont sûr d’eux, charismatiques et ambitieux. Ils peuvent aussi être généreux et sont généralement des gens très positifs, mais parfois les Lions sont aussi colériques, dominateurs, mélodramatiques et sensible à la critique.</p>

					<h2 id="Vierge">Vierge</h2>
					<h3>Le critique</h3>
					<p>Les natifs du signe astrologique de la Vierge sont ordonnés, analytiques, créatifs et ont souvent un talent artistique pour un instrument de musique ou pour la peinture, ils sont aussi fiables et sensibles. En dépit de leurs traits positifs, les Vierges peuvent parfois paraître froides, difficiles et étroites d’esprit.</p>

					<h2 id="Balance">Balance</h2>
					<h3>Le diplomate</h3>
					<p> Les natifs du signe astrologique de la Balance sont généralement charmants et diplomates, flexibles et ont du tact. Les Balances ont tendance à être idéalistes et peuvent être indécis et peu fiables à la fois.</p>

					<h2 id="Scorpion">Scorpion</h2>
					<h3>Le crinqué</h3>
					<p>Le signe astrologique du Scorpion est sûrement le plus compliqué des douze signes du zodiaque. Les natifs du Scorpion sont passionnés, loyaux et sages, mais peuvent aussi être extrêmement jaloux, obsessionnels, méfiants et avides de pouvoir.</p>

					<h2 id="Sagittaire">Sagittaire</h2>
					<h3>L'aventurier</h3>
					<p>Les Sagittaires sont généralement indépendants, optimistes et aiment l’aventure, mais ils peuvent aussi parfois sembler insensibles et manquer de tact car ils sont plutôt directs et disent ce qu’ils pensent sans détour.</p>

					<h2 id="Capricorne">Capricorne</h2>
					<h3>Le prudent</h3>
					<p>Les Capricornes sont ambitieux et ingénieux, responsables, patients et organisés. Ils peuvent également être coincés, méfiants, sans imagination et très prudents.</p>

					<h2 id="Verseau">Verseau</h2>
					<h3>L'humanitariste</h3>
					<p>Les Verseau sont souvent intelligents et indépendants mais aussi sympathiques. Souvent intéressés par la philanthropie ou par des causes humanitaires. Les natifs du signe astrologique du Verseau semblent souvent impassibles et détachés et affichent peu leurs émotions par rapport aux personnes nées sous les autres signes astrologiques.</p>
					
					<h2 id="Poisson">Poisson</h2>
					<h3>L'impressionable</h3>
					<p>Le signe astrologique du Poisson est un signe d’eau. Les natifs du Poisson sont habituellement compatissants, dévoués, protecteurs et sensibles, ils sont peut-être parfois trop sensibles, mais peuvent être orgueilleux ou pessimistes et dévoilent rarement leurs sentiments aux autres.</p>
